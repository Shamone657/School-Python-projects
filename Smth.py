Name = []

Director = []

Genre = []

Date = []

Score = []

for x in range(5):
    
    name = input("Enter the name of your favourite movie:\n")
    
    director = input("Enter the name of the director for that movie:\n")
    
    genre = input("Enter the genre of the movie:\n")

    date = input("Enter the release date for the movie:\n")
    
    score = int(input("Enter the IMBD score of the movie:\n"))
    
    Name.append(name)
    
    Director.append(director)
    
    Genre.append(genre)
    
    Date.append(date)
    
    Score.append(score)
    
print(Name)

